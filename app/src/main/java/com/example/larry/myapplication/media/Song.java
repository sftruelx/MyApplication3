public class Song {

    private String ic_url;

    private String song_title;

    private String song_artist;

    private String song_url;

    private String song_classify;

    public String getIc_url() {
        return ic_url;
    }

    public void setIc_url(String ic_url) {
        this.ic_url = ic_url;
    }

    public String getSong_title() {
        return song_title;
    }

    public void setSong_title(String song_title) {
        this.song_title = song_title;
    }

    public String getSong_artist() {
        return song_artist;
    }

    public void setSong_artist(String song_artist) {
        this.song_artist = song_artist;
    }

    public String getSong_url() {
        return song_url;
    }

    public void setSong_url(String song_url) {
        this.song_url = song_url;
    }

    public String getSong_classify() {
        return song_classify;
    }

    public void setSong_classify(String song_classify) {
        this.song_classify = song_classify;
    }
}
